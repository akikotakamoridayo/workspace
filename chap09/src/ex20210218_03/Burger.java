package ex20210218_03;

public class Burger {
	private String burgerName;
	private int cal;
	private int cost;

	public String getBurgerName() {
		return burgerName;
	}
	public void setBurgerName(String burgerName) {
		this.burgerName=burgerName;
	}
	public int getCal() {
		return cal;
	}
	public void setCal(int cal) {
		this.cal=cal;
	}
	public int getcost() {
		return cost;
	}
	public void setcost(int cost) {
		this.cal=cost;
	}
}
